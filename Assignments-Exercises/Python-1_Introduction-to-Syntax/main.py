# This program explores Python syntax, variables, data types, and basic operators.

# 1. Declare variables of different types
name = "Kenneth"  # string
age = 21        # integer
height = 5.4     # float
is_student = True  # boolean

# 2. Perform basic arithmetic operations
sum_result = age + height
product_result = age * height

# 3. Concatenate strings and display outputs
greeting = "\nHello, " + name + "!"
print(greeting)

# 4. Display results of arithmetic operations
print("\nSum of age and height:", sum_result)
print("\nProduct of age and height:", product_result)

# 5. Display the types of each variable
print("\nType of name:", type(name))
print("\nType of age:", type(age))
print("\nType of height:", type(height))
print("\nType of is_student:", type(is_student), "\n")
